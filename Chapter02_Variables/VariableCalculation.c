#include <stdio.h>

int main()
{
    float a = 5.0f;
    float b = 2.0f;
    float c;

    // Addition
    c = a + b;
    printf("%f\n", c);

    // Subtraction
    c = a - b;
    printf("%f\n", c);

    // Multiplication
    c = a * b;
    printf("%f\n", c);

    // Division
    c = a / b;
    printf("%f\n", c);

    return 0;
}
