#include <stdio.h>

int main()
{
    // Declaration.
    // int (32bit, 4 Byte)
    int my_age;

    // Init.
    my_age = 28;

    // int (32bit, 4 Byte)
    unsigned int my_age2 = 28U;

    // floating point number
    // float (32 bit / 4 Byte)
    float my_balance1 = 1750.30F;

    // double (64 bit / 8 Byte)
    double my_balance2 = 1750.30;

    return 0;
}
